package parameters;

public abstract class Parameters {

	protected int numRodadas;
	protected int numRodadasTotal;
	protected int numTeams;
	protected int numTeamsTotal;

	public int getNumRodadas() {
		return numRodadas;
	}

	public int getNumRodadasTotal() {
		return numRodadasTotal;
	}

	public int getNumTeams() {
		return numTeams;
	}

	public int getNumTeamsTotal() {
		return numTeamsTotal;
	}

}
